public class date {
    private int minute;
    private int second;
    private int hour;

    public int getMinute(){
        return minute;
    }

    public void setMinute(int min){
        minute = min;
    }

    public int getSecond(){
        return second;
    }

    public void setSecond(int sec){
        second = sec;
    }

    public int getHour(){
        return hour;
    }

    public void setHour(int hr){
        hour = hr;
    }
}
